     public void Tabla(){
    ArrayList<producto> datos = cl.consulta();
    String titulos []= { "Codigo","Nombre" , "Cantidad" , "Lugar de Producci√≥n" , "Fecha de Ingreso"};
        
    
     tabla_modelo  = new DefaultTableModel(null,titulos);
     
     for( producto general: datos ){
             Object informacion [] = new Object[5];
             informacion[0] = general.getCodigo();
             informacion[1] = general.getNombre();
             informacion[2] = general.getCantidad();
             informacion[3] = general.getLugar();
             informacion[4] = general.getFecha();
             tabla_modelo.addRow(informacion);
     }
    
    tabla_primaria.setModel(tabla_modelo);
    
    }
     
     
     public void mostrar(){
    cl.buscar(Integer.parseInt(tabla_primaria.getValueAt(tabla_primaria.getSelectedRow(),0)+ ""));
     dispose();
    }
    public void eliminar(){
    cl.buscar1(Integer.parseInt(tabla_primaria.getValueAt(tabla_primaria.getSelectedRow(),0)+ ""));
     dispose();
    }
